const LANG_MAP = {
  'English': 'English',
  'Russian': 'Russian',
  'Русский': 'Russian',
  'Azerbaijani': 'Azerbaijani',
  'Azərbaycanca': 'Azerbaijani',
  'az': 'Azerbaijani',
  'ru': 'Russian',
  'en': 'English',
};

export function useT() {
  const [, forceUpdate] = useState(0);

  useEffect(() => {
    const handler = () => forceUpdate(n => n + 1);
    window.addEventListener('language-changed', handler);
    return () => window.removeEventListener('language-changed', handler);
  }, []);

  const ctx = useContext(SettingsContext);
  const raw = ctx?.prefs?.language || localStorage.getItem('jar_language') || 'English';
  const lang = LANG_MAP[raw] || 'English';
  const dict = T[lang] || T.English;
  return (key) => dict[key] ?? T.English[key] ?? key;
}